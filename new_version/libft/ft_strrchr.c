#include "libft.h"

static long int	ft_slen(const char *s)
{
	long int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

char	*ft_strrchr(const char *s, int c)
{
	long int	i;

	i = ft_slen(s);
	while (i >= 0)
	{
		if (((s[i] - (char)c) == 0) && (((char)c - s[i]) == 0))
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
